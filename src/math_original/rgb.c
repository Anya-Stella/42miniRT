 #include "minirt.h"

 t_rgb3	add_rgb(t_rgb3 c1, t_rgb3 c2)
 {
	int r;
	int g;
	int b;

	r = fmin(c1.r + c2.r, 255);
	g = fmin(c1.g + c2.g, 255);
	b = fmin(c1.b + c2.b, 255);
	return ((t_rgb3){r, g, b});
 }
 